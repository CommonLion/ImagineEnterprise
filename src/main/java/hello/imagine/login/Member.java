package hello.imagine.login;

public class Member {
    String name;
    String id;
    String birthDate;
    String pw;
    String email;
    String nickname;

    public Member(String name, String id, String birthDate, String pw, String email, String nickname) {
        this.name = name;
        this.id = id;
        this.birthDate = birthDate;
        this.pw = pw;
        this.email = email;
        this.nickname = nickname;
    }


}
